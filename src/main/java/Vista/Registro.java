    private void enviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enviarActionPerformed
        String nombre, correo, usuario, pass;
        nombre = txtnombre.getText();
        correo = txtcorreo.getText();
        usuario = txtusuario.getText();
        pass = Arrays.toString(txtpass.getPassword());

        if (txtnombre.getText().equals("") || (txtcorreo.getText().equals("")) || (txtusuario.getText().equals(""))
                || (txtpass.getPassword().equals(""))) {
            javax.swing.JOptionPane.showMessageDialog(this, "Debe llenar todos los campos \n", "AVISO!", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            txtnombre.requestFocus();
        } else {

            String SQL_INSERT = "INSERT INTO `usuarios`(`nombre`, `correo`, `clave`, `usuario`) VALUES (?, ?, ?, ?)";
            JOptionPane.showMessageDialog(null, "usuario registrado exitosamente ");

            Connection con = null;
            PreparedStatement ps = null;
            try {
                con = ConnectionDB.getConnection();
                ps = con.prepareStatement(SQL_INSERT);
                ps.setString(1, nombre);
                ps.setString(2, correo);
                ps.setString(3, pass);
                ps.setString(4, usuario);
                ps.executeUpdate();
            } catch (SQLException ex) {
                Logger.getLogger(Registro.class.getName()).log(Level.SEVERE, null, ex);
            } finally {
                try {
                    ConnectionDB.close(ps);
                    ConnectionDB.close(con);
                } catch (SQLException ex) {
                    Logger.getLogger(Registro.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
        
        IniciarSesion ventana = new IniciarSesion();
        ventana.setVisible(true);
        this.setVisible(false);
    }//GEN-LAST:event_enviarActionPerformed

